class Clothing():
    """A simulation of an article of clothing.

    Attributes:
        color (str) = The clothing article's color.
    """
    def __init__(self, color):
        self.color = color

class Hat(Clothing):
    pass

class Shirt(Clothing):
    pass

class Pants(Clothing):
    pass

class Coat(Clothing):
    pass

class Socks(Clothing):
    pass

class Shoes(Clothing):
    pass
